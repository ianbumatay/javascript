// While loop 

// The while loop creates a loop that is executed as long as a specified condition evaluates to true. 
// The loop will continue to run until the condition evaluates to false. 
// The condition is specified before the loop, and usually, some variable is incremented or altered in the
//  while loop body to determine when the loop should stop. 


let i = 0 // inizialise the begining of index 

while(i < 5){
    console.log(i)
    i++
}